//纯数学题，！！！fk
#include <stdio.h>
int main() 
{
	double sum = 0;
	double x;
	int k;
	for (x = 0.000; x <= 2.000; x += 0.001) 
	{
		sum = 0;
		//分母变成k的立方，则只需要10000次就可以达到e-12的精度，比原来平方降低了100倍运算
		for (k = 1; k < 10000; k++) 
		{
			sum += (1.0 - x) / (k * (k + 1) * (k + x));
		}
		//(1 - x) / (2 * 10000 * 10000)为10000项以后的逼近，能保证精度
		sum = sum + (1 - x) / (2 * 10000 * 10000) + 1.0;
		//f表示输出浮点数，
		//5表示最小输出字符宽度为5位数，
		//3表示浮点数输出小数点后为3位数
		printf("%5.3f %16.12f\n", x, sum);
	}
	return 0;
}
